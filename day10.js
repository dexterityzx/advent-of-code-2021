const leftToRight = new Map([
  ["{", "}"],
  ["[", "]"],
  ["<", ">"],
  ["(", ")"],
]);

const completedCharScores = new Map([
  [")", 1],
  ["]", 2],
  ["}", 3],
  [">", 4],
]);

const findIncompletedLines = (lines) =>
  lines.filter((line) => getCorruptedScore(line) === 0);

const findCharsToCompleteLine = (line) => {
  const stack = [];
  for (let i = 0; i < line.length; i++) {
    const char = line[i];
    if (isLeft(char)) stack.push(char);
    else stack.pop();
  }

  return stack.map((c) => leftToRight.get(c)).reverse();
};

const calculateCompletedScore = (charsToComplete) =>
  charsToComplete
    .map((c) => completedCharScores.get(c))
    .reduce((sum, cur, i) => sum * 5 + cur, 0);

const completedScores = findIncompletedLines(lines)
  .map(findCharsToCompleteLine)
  .map(calculateCompletedScore);
  
completedScores.sort((a, b) => a-b);

