const lines = input.map((line) => line.split(""));

const rightToLeft = new Map([
  ["}", "{"],
  ["]", "["],
  [">", "<"],
  [")", "("],
]);

const corruptedCharScores = new Map([
  [")", 3],
  ["]", 57],
  ["}", 1197],
  [">", 25137],
]);

const isLeft = (s) => s === "{" || s === "[" || s === "<" || s === "(";

const getCorruptedScore = (line) => {
  const stack = [];

  for (let i = 0; i < line.length; i++) {
    const char = line[i];
    // char is left
    if (isLeft(char)) {
      stack.push(char);
      continue;
    }
    // char is right
    const left = stack.pop();
    if (left !== rightToLeft.get(char)) {
      return corruptedCharScores.get(char);
    }
  }

  return 0;
};

const calculateCorruptedScore = (lines) =>
  lines.map(getCorruptedScore).reduce((s, c) => s + c, 0);

console.log(calculateCorruptedScore(lines));

